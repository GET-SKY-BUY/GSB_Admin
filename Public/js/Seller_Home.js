function validatePhoneNumber(number) {
    const re = /^[0-9]{10}$/; // Assuming a 10-digit phone number
    return re.test(String(number));
}


function validateEmail(email) {
    const re = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    return re.test(String(email).toLowerCase());
}


